export function validateregex(controlName, regex) {
  return (formGroup: FormGroup) => {
    var regexExp = new RegExp(regex);
    const control = formGroup.controls[controlName];

    if (control.errors) {
      return;
    }
    else if (regexExp.test(control.value)) {
      control.setErrors(null);
    } else if (!regexExp.test(control.value)) {
      control.setErrors({ validateregex: true });
    }
  }
}

